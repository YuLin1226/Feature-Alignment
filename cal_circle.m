function [xc, yc, r] = cal_circle(x, y)

%% Matrix 算法
    A = [   2*x(1), 2*y(1), 1;
            2*x(2), 2*y(2), 1;
            2*x(3), 2*y(3), 1];
    
    B = [   x(1)^2 + y(1)^2 ;
            x(2)^2 + y(2)^2 ;
            x(3)^2 + y(3)^2 ];
       
    X = inv(A) * B;
    xc = X(1);
    yc = X(2);
    r  = ( X(3) + xc^2 + yc^2 )^0.5;

